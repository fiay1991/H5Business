package com.park.h5business.response;

public class EIPublicResponse {
	
	private String code;
	private String msg;
	private EInvoiceResponse data;
	
	public EIPublicResponse(){
		
	}
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}

	public EInvoiceResponse getData() {
		return data;
	}

	public void setData(EInvoiceResponse data) {
		this.data = data;
	}
	
	
}
